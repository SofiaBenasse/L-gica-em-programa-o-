//ESTRUTURAS DE REPETIÇÃO FOR/ WHILE/ DO WHILE 
//FOR
for (let contador = 0; contador < 4; contador++){
  console.log(contador)
}

let pontosDeVida = 0
for (let i = 0; i < 11; i++){
  pontosDeVida += 1
  console.log ("Tomou poção mágica " + i)
 
}
 console.log (pontosDeVida + " Totais") 

 //WHILE
 let cont = 0
 while (cont < 3 ){ 
  cont++
  console.log("olá")
 }


